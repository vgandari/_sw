function [M, F, f2] = forcemoment(I, C, R)
    f2 = 3/norm(R)^2*[1/2*( I(1, 1)*(1 - 3*C(1, 1)^2) + I(2, 2)*(1 - 3*C(1, 2)^2) + I(3, 3)*(1 - 3*C(1, 3)^2) );
        I(1, 1)*C(2, 1)*C(1, 1) + I(2, 2)*C(2, 2)*C(1, 2) + I(3, 3)*C(2, 3)*C(1, 3);
        I(1, 1)*C(3, 1)*C(1, 1) + I(2, 2)*C(3, 2)*C(1, 2) + I(3, 3)*C(3, 3)*C(1, 3)];
    
    F = -1/norm(R)^2*([1; 0; 0] + f2*(100/1000)^2);
    
    M = -cross(R, F);
end